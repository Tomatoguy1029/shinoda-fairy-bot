export interface MessageVars {
	item?: string;
	items?: string;
	time?: string;
	name?: string;
}

/** プレースホルダを埋めるユーティリティ */
function fillVars(text: string, vars: MessageVars): string {
	let result = text;
	if (vars.item !== undefined) result = result.replaceAll('{item}', vars.item);
	if (vars.items !== undefined) result = result.replaceAll('{items}', vars.items);
	if (vars.time !== undefined) result = result.replaceAll('{time}', vars.time);
	if (vars.name !== undefined) result = result.replaceAll('{name}', vars.name);
	return result;
}

/** プールから一様ランダムに選び、プレースホルダを埋める */
export function pick(id: SituationId, vars: MessageVars = {}): string {
	const pool = pools[id];
	const text = pool[Math.floor(Math.random() * pool.length)];
	return fillVars(text, vars);
}

/**
 * NAG系メッセージを催促回数に応じた段階で選ぶ。
 * nagCount: このリマインドサイクルで何回目の催促か (1始まり)
 *   1回目      → mild(穏やか)
 *   2〜3回目   → annoyed(イラつき)
 *   4回目以降  → furious(ブチギレ)
 */
export function pickNag(id: NagId, nagCount: number, vars: MessageVars = {}): string {
	const pool = nagPools[id];
	let level: string[];
	if (nagCount <= 1) {
		level = pool.mild;
	} else if (nagCount <= 3) {
		level = pool.annoyed;
	} else {
		level = pool.furious;
	}
	const text = level[Math.floor(Math.random() * level.length)];
	return fillVars(text, vars);
}
